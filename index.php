										<h2 class="major">Why Chattanooga?</h2>
										<p>Our members are Chattanoogans who believe that democratic socialism will gain support in Chattanooga. We want to connect and support the fights for equality and justice already underway in Chattanooga. We want to add our own perspective where it's welcome.</p>
										<p>Chattanooga is growing rapidly. It's up to us to make sure that the growth does not become a nightmare of inequality. By demanding a say in Chattanooga's destiny, we ensure that this growth leaves no one behind.</p>
